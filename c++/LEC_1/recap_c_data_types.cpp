#include <cstdio>
int main()
{
    int x=20;
    printf("int print format :%d\n",x);
    char s='d';
    printf("char print format :%c\n",s);
    short ss=123;
    printf("short print format :%hd\n",ss);
    long sf=123999999;
    printf("long print format :%ld\n",sf);
    long long hs=1237777777777777;
    printf("long long print format :%lld\n",hs);
    float js=12.3;
    printf("float print format :%f\n",js);
    double ls=123888.88888;
    printf("double print format :%lf\n",ls);
    long double ks=123999999999999999999999.9999;
    printf("long double print format :%Lf\n",ks);
    short ns=0.123;
    printf("bool print format :%d\n",ns);
    
    return 0;   
    
    
    


}